#include "3-calc.h"

/**
 * op_add - adds a and b
 * @a: int 1
 * @b: int 2
 * Return: the sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts a from b
 * @a: int 1
 * @b: int 2
 * Return: a minus b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies a and b
 * @a: int 1
 * @b: int 2
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides a into b
 * @a: int 1
 * @b: int 2
 * Return: a divided by b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulos a & b
 * @a: int 1
 * @b: int 2
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
